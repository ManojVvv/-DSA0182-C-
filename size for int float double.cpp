#include <iostream>
using namespace std;
int main() {
    int integerType;
    float floatType;
    double doubleType;
    char charType;
    cout << "Size of int: " << sizeof(integerType) << " bytes" << endl;
    cout << "Size of float: " << sizeof(floatType) << " bytes" << endl;
    cout << "Size of double: " << sizeof(doubleType) << " bytes" << endl;
    cout << "Size of char: " << sizeof(charType) << " byte" << endl;
    return 0;
}

